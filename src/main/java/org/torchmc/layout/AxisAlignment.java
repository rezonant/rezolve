package org.torchmc.layout;

/**
 * Represents an alignment along an axis
 */
public enum AxisAlignment {
    /**
     * Align elements to the start of this axis (the low-coordinate end, left for X and top for Y)
     */
    START,

    /**
     * Align elements to the center of this axis
     */
    CENTER,

    /**
     * Align elements to the end of this axis (the high-coordinate end, right for X and bottom for Y)
     */
    END
}
